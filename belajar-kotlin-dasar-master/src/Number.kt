fun main() {
    var age: Int = 200

    var sample: Float = 10.11F

    println(age)
    println(sample)

    var binary: Int = 0b010101010
    println(binary)

    var price: Long = 9_000_000_000L;
    println(price)

    var priceInt: Int = price.toInt()
    println(priceInt)

    var doubleBinary: Double = binary.toDouble()
    println(doubleBinary)
}